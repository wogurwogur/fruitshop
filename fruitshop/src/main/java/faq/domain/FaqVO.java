package faq.domain;

public class FaqVO {
	
	 private int faq_no;
	 private String faq_title;
	 private String faq_contents;
	 private String faq_regidate;
	 private int faq_status;
	 private int faq_viewcount;
	 
	 
	public int getFaq_no() {
		return faq_no;
	}
	public void setFaq_no(int faq_no) {
		this.faq_no = faq_no;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_contents() {
		return faq_contents;
	}
	public void setFaq_contents(String faq_contents) {
		this.faq_contents = faq_contents;
	}
	public String getFaq_regidate() {
		return faq_regidate;
	}
	public void setFaq_regidate(String faq_regidate) {
		this.faq_regidate = faq_regidate;
	}
	public int getFaq_status() {
		return faq_status;
	}
	public void setFaq_status(int faq_status) {
		this.faq_status = faq_status;
	}
	public int getFaq_viewcount() {
		return faq_viewcount;
	}
	public void setFaq_viewcount(int faq_viewcount) {
		this.faq_viewcount = faq_viewcount;
	}
	 
	 
	
}
