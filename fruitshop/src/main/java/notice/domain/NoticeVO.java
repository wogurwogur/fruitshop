package notice.domain;

public class NoticeVO {
	
	private int notice_no;
	private String notice_title;
	private String notice_contents;
	private String notice_regidate;
	private int notice_status;
	private int notice_viewcount;
	 
	 
	 
	 
	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_contents() {
		return notice_contents;
	}
	public void setNotice_contents(String notice_contents) {
		this.notice_contents = notice_contents;
	}
	public String getNotice_regidate() {
		return notice_regidate;
	}
	public void setNotice_regidate(String notice_regidate) {
		this.notice_regidate = notice_regidate;
	}
	public int getNotice_status() {
		return notice_status;
	}
	public void setNotice_status(int notice_status) {
		this.notice_status = notice_status;
	}
	public int getNotice_viewcount() {
		return notice_viewcount;
	}
	public void setNotice_viewcount(int notice_viewcount) {
		this.notice_viewcount = notice_viewcount;
	}
	
}
